package Strings;

public class Concatenation {
	public static void main(String[] args) {
		String firstName = "Ravindra";
		String lastName = "Chavhan";
		String fullName = firstName + " " + lastName;

		System.out.println(fullName);

		String s1 = "Vision";
		String s2 = "Raj";
		String s3 = "2020";
		System.out.println(s1 + s2 + s3);

		System.out.println(s1 + "2024");

		System.out.println(s1 + "2030" + s2 + "50");
		System.out.println("35" + s1 + "30" + s2);
		System.out.println(s1 + "200" + s2 + "30");
		System.out.println(s1 + s2 + "5");
		System.out.println("5" + s1 + s2);
		System.out.println(s1 + "10" + s2 + "5030");
		System.out.println(s1 + "200" + s2 + "30");
	}
}
