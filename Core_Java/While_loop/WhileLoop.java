package While_loop;

public class WhileLoop {

	public static void main(String[] args) {
		// Till the time the condition is true, while loop going to run

		int i = 1;
		while (i <= 10) { // i<=10 is a condition & till it is true while loop goes on printing the value i =1	
			System.out.println(i);
			/* Hence we have to increment the value of i so that at one time value will be greater than 10 and 
			 then while loop will come out from that and stop.
			 */
			i++;
			
			// ; act as a break point for loop
			int a = 1;
			
			while(a<=10);
			
			System.out.println(a);
			
			a++;
		}
	}

}
