package For_Loop;

public class Numbers {
   public static void main(String[] args) {
      int i;
      for(i = 1; i <= 10; ++i) 
      {
    	  System.out.println(i); // Ascending numbers
      }
      
      System.out.println("..................." );

      for(i = 10; i > 0; --i) {
         System.out.println(i);  // getting reverse numbers.
      }

   }
}
