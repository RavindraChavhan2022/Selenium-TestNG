package Assignments;

public class ASCII_Value {

	public static void main(String[] args) {
		// ASCII value from character
		char ch = 'a';
		int ascii = ch;
		System.out.println(ascii);
		
		char ch1 = 'R';
		int ASCII = ch1;
		System.out.println(ASCII);
		
		// Character from an ASCII value
		int ASCII1 = 89;
		char character = (char)ASCII1;
		System.out.println(character);
		
		int ASCII2 = 112;
		char ch2 = (char)ASCII2;
		System.out.println(ch2);

	}

}
