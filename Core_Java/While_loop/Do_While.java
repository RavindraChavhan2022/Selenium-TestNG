package While_loop;

public class Do_While {

	public static void main(String[] args) {

		int i = 1;
		do {
			System.out.print(i + " ");
			i++;
		} while (i <= 10); // Dont put {} after while loop
		
		System.out.println();
	
		char ch = 'A';
		do {
			System.out.print(ch + " ");
			ch++;
		}while(ch<='Z');

	}

}
