package Assignments;

public class Even_odd {

	public static void main(String[] args) {
		
		int n = 257;
		 
	      if (n % 2 == 0) 
	      {
	         System.out.println("The number " + n + " is Even");
	      } 
	      else 
	      {
	         System.out.println("The number " + n + " is Odd");
	      }
	}

}
