package For_Loop;

public class Even_numbers {
   public static void main(String[] args) {
	   
	 //Print odd numbers between 100 and 150
      for(int i = 100; i >= 100 && i <= 150; i += 2) 
      {
         System.out.println(i);
      }
      
      //print odd numbers between 100 and 150
      for(int a = 101; a >= 100 && a <= 150; a += 2) 
      {
         System.out.println(a);
      }

   }
}
