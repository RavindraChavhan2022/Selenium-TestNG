package Operators;

public class Arithmetic_operators {
   public static void main(String[] args) {
      int a = 30;
      int b = 25;
      System.out.println(a + b);
      System.out.println(a * b);
      System.out.println(a - b);
      System.out.println(a / b);
      System.out.println(a % b);
      a = 50;
      b = 20;
      System.out.println(a + b);
      System.out.println(a * b);
      System.out.println(a - b);
      System.out.println(a / b);
      System.out.println(a % b);
   }
}
