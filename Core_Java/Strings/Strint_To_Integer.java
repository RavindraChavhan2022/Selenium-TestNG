package Strings;

public class Strint_To_Integer {

	public static void main(String[] args) {

		String str1 = "1989";
		int i = Integer.parseInt(str1);
		System.out.println(i);
		//
		String str2 = Integer.toString(i);
		System.out.println(str2);

	}

}

//The parseInt() method converts a String into an integer and the toString() method converts an Integer into a String.
