package Datatypes;

public class DataTypes {
   public static void main(String[] args) {
      int i = 28;
      double d = 38.7844D;
      float f = 98.0F;
      char c = 'C';
      boolean b = true;
      String s = "Ajay";
      System.out.println(i);
      System.out.println(d);
      System.out.println(f);
      System.out.println(c);
      System.out.println(b);
      System.out.println(s);
   }
}
